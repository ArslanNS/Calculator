package operation;

/**Class to divide float values.
 * @author zfac237
 *
 */
public class Divide implements Calculator {
  
  /** evaluate method to divide two float values.
   * @paramf1 
   * @paramf2
   * @return float that is f1 divided by f2.
   */
  public float evaluate(float f1, float f2) {
    return f2 / f1;
  }
}
