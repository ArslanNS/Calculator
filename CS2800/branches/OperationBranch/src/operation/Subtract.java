package operation;

/**Class to subtract float values.
 * @author zfac237
 *
 */
public class Subtract implements Calculator {
  
  /** evaluate method to subtract two float values.
   * @paramf1 
   * @paramf2
   * @return float that is f1 subtracted by f2.
   */
  public float evaluate(float f1, float f2) {
    return f1 - f2;
  }
}
