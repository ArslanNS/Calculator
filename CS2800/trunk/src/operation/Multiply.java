package operation;

/**Class to multiply float values.
 * @author zfac237
 *
 */
public class Multiply implements Calculator {
  
  /** evaluate method to multiply two float values together.
   * @paramf1 
   * @paramf2
   * @return float that is f1 and f2 multiplied together
   */
  public float evaluate(float f1, float f2) {
    return f1 * f2;
  }
}
